import mongoose from "mongoose";
import { getConnection } from "./db.js";

export const userSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
    trim: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  id: {
    type: String,
    required: true,
  },  
});

export const User = mongoose.model("Question", userSchema);